                <h1 class="text-lg mt-4">De acuerdo con el reporte EduTrends (2014) los MOOCs, cursos masivos,
                    abiertos, en línea por sus siglas en inglés, son clases “impartidas a través de plataformas
                    tecnológicas que habilitan el proceso de enseñanza-aprendizaje a miles de estudiantes”. </h1>
                <h1 class="text-xl font-bold">En ese sentido, basándonos en la metodología de los MOOCs, con este
                    proyecto se pretende lanzar una iniciativa en el seno de la UNESR núcleo Palo Verde, inicialmente en
                    las asignaturas del ciclo profesional de la carrera administración mención informática, para
                    complementar y perfeccionar los conocimientos que los facilitadores de la UNESR transmiten o
                    imparten a los participantes, así como, a todo aquel que quiere estar actualizado sobre un tema en
                    particular. Este proyecto (CyberUNESR) es una plataforma de aprendizaje virtual desarrollada para la
                    UNESR, para que esta y sus facilitadores puedan brindar cursos en línea amplios y abiertos al
                    público, cuyo principal objetivo es complementar las clases presenciales y así mantener a los
                    participantes involucrados en su aprendizaje desde cualquier dispositivo, desde cualquier lugar del
                    mundo, en cualquier momento, gracias a lo cual el participante puede tomar el curso a su propio
                    ritmo
                    y con la dedicación que desee, de esta manera mejorar sus competencias académicas.
                </h1>
                <h3>Un MOOC se caracteriza por: <br>
                    • Es un curso: Cuenta con una estructura orientada al aprendizaje, y debe conllevar unas pruebas
                    &nbsp;&nbsp;&nbsp;o
                    evaluaciones que acrediten las habilidades y competencias adquiridas.<br>
                    • Es masivo: El número de posibles matriculados es ilimitado.<br>
                    • En línea: El curso es a distancia, y no requiere la asistencia a un aula.<br>
                    • Abierto: Los materiales son accesibles de forma gratuita en Internet. Ello no implica que
                    &nbsp;&nbsp;&nbsp;puedan ser reutilizados en otros cursos.<br>
                </h3>
                <h3>
                    Por ultimo, ¿Por qué los MOOC son interesantes?, según diversos autores estos están diseñados con
                    doble objetivo:<br>
                    Por un lado, la difusión del conocimiento entre los participantes y la sociedad, viene con nuevas
                    audiencias, así pues, mejora la reputación de la universidad como una entidad creativa, fuente de
                    conocimiento de alta calidad. <br>
                    Por otro lado, posiblemente puede servir a los facilitadores como una herramienta de marketing para
                    promover su enseñanza y publicaciones, de esta manera llamar la atención de nuevos estudiantes que
                    estén interesados en otros cursos ofrecidos por el facilitador a cambio de una remuneración.
                </h3>

